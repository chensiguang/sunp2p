package com.sunyard.directbank.enums;


/*
 * 0-	募集
1-	开放
2-	封闭
3-	募集结束
4-	产品终止
5- P2p募集
6- P2p产品成立
 */
public enum PRODSTATE {
	Recruitment("0"),
	Open("1"),
	obturate("2"),
	Ended("3"),
	Terminate("4"),
	P2pRecruitment("5"),
	P2pEstablish("6");
	
	private String code;
	
	private PRODSTATE(String code){
		this.code= code;
	}
	
	public String getCode(){
		return code;
	}
}
